"use client";

import { Card, CardContent } from "@/components/ui/card";
import { Sparkles, AlertTriangle } from "lucide-react";

interface InsightGeneratorProps {
  crop: string;
  confidence: number;
  explanation?: Record<string, number>;
}

export function InsightGenerator({ crop, confidence, explanation }: InsightGeneratorProps) {
  if (!explanation || Object.keys(explanation).length === 0) return null;

  // Find top 2 driving factors
  const entries = Object.entries(explanation);
  const sortedFactors = entries.sort((a, b) => Math.abs(b[1]) - Math.abs(a[1]));
  const primaryFactor = sortedFactors[0];
  const secondaryFactor = sortedFactors[1];



  return (
    <Card className="bg-primary/5 border-primary/20 overflow-hidden relative">
      <div className="absolute top-0 right-0 p-3 opacity-10">
        <Sparkles className="w-16 h-16 text-primary" />
      </div>
      <CardContent className="pt-6 relative z-10">
        <div className="flex items-start gap-4">
          <div className="bg-primary/20 p-2 rounded-lg">
            <Sparkles className="w-5 h-5 text-primary" />
          </div>
          <div className="space-y-2">
            <h4 className="font-semibold text-foreground flex items-center gap-2">
              AI Analysis
              <span className="text-xs font-normal text-muted-foreground bg-background/50 px-2 py-0.5 rounded-full border border-border">
                Generated by AgroSense
              </span>
            </h4>
            <p className="text-sm text-foreground/80 leading-relaxed">
              The model has <strong>{Math.round(confidence * 100)}% confidence</strong> in recommending{" "}
              <strong className="capitalize text-primary">{crop}</strong>. 
              This decision was primarily driven by the 
              <span className="font-mono text-primary mx-1">{primaryFactor[0]}</span> 
              level, which had the most significant impact.
              {secondaryFactor && (
                <>
                   {" "}Secondary influence came from <span className="font-mono text-primary mx-1">{secondaryFactor[0]}</span>.
                </>
              )}
            </p>
            
            {/* Warning / Suggestion Placeholder - Logic could be expanded */}
            {confidence < 0.7 && (
              <div className="mt-3 flex items-center gap-2 text-xs text-amber-500 bg-amber-500/10 p-2 rounded border border-amber-500/20">
                <AlertTriangle className="w-4 h-4" />
                <span>Confidence is moderate. Consider validating soil sample quality.</span>
              </div>
            )}
          </div>
        </div>
      </CardContent>
    </Card>
  );
}
